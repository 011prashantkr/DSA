package PraticeQuestions.Loops;

public class sumOfEvenNo {
    public static void main(String[]args){

        int n=4;
        int sum=0;
        for(int i=0;i<n;i++){
                sum=sum+(2*i);//sum of n even no logic starting from 0
            }
        System.out.println(sum);
        }

    }

