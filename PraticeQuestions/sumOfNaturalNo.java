package PraticeQuestions;

public class sumOfNaturalNo {
    public static void main(String[] args) {
        int n=5;
        int sum =0;
        for(int i=0;i<=n;i++){
            sum=(sum+i);
            System.out.println("the of natural no is " +i);
        }
        System.out.println( "sum of %d natural no "+sum);
    }
}


