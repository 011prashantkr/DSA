package Strings;

public class StringConcatenation   {
    public static void main(String[] args) {
        String s1="Prashant ";
        String s2="kumar";
        //by using concatenation (+) operator
        System.out.println(s1+s2);

        //by using concatenation method ==>concat()
        System.out.println(s1.concat(s2));


    }
}
